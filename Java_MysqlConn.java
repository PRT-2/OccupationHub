    private void jbtnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnAddActionPerformed
        
        if (jtxtAdvertisement.getText().equals("")||jtxtCompanyname.getText().equals("")||jtxtNumber.getText().equals("")||jtxtEmail.getText().equals("")){
                JOptionPane.showMessageDialog(this, "please enter all DATA");
        }else{
            String data[] = {jtxtAdvertisement.getText(), jtxtCompanyname.getText(), jtxtNumber.getText(), jtxtEmail.getText()};
            DefaultTableModel tblModel = (DefaultTableModel)jTable1.getModel();
            tblModel.addRow(data);
            JOptionPane.showMessageDialog(this, "ADD DATA SUCCESSFULLY");
            jtxtAdvertisement.setText("");
            jtxtCompanyname.setText("");
            jtxtNumber.setText("");
            jtxtEmail.setText("");
        }
        
        try
        {
            Class.forName("com.mysql.jdbc.Driver");
            sqlConn = DriverManager.getConnection(dataConn, username, password);
            pst = sqlConn.prepareStatement("insert into connector(Advertisement, Companyname, Number, Email)value(?,?,?,?)");
            
            pst.setString(1, jtxtAdvertisement.getText());
            pst.setString(2, jtxtCompanyname.getText());
            pst.setString(3, jtxtNumber.getText());
            pst.setString(4, jtxtEmail.getText());
            
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Record Added");
            upDateDB();
        }
        catch (ClassNotFoundException ex) {
            java.util.logging.Logger.getLogger(Java_MysqlConn.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        }catch (SQLException ex){
            java.util.logging.Logger.getLogger(Java_MysqlConn.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        }    
    }//GEN-LAST:event_jbtnAddActionPerformed

