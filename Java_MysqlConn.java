    public void upDateDB()
    {
        try
        {
            Class.forName("com.mysql.jdbc.Driver");
            sqlConn = DriverManager.getConnection(dataConn, username, password);
            pst = sqlConn.prepareStatement("select * from connector");
            
            rs = pst.executeQuery();
            ResultSetMetaData stData = rs.getMetaData();
            
            q = stData.getColumnCount();
            
            DefaultTableModel RecordTable = (DefaultTableModel)jTable1.getModel();
            RecordTable.setRowCount(0);
            
            while(rs.next()){
                Vector coloumnData = new Vector();
                
                for (i = 1; i <= q; i++)
                {
                   
                    coloumnData.add(rs.getString("Advertisement"));
                    coloumnData.add(rs.getString("Companyname"));
                    coloumnData.add(rs.getString("Number"));
                    coloumnData.add(rs.getString("Email"));
                }
                RecordTable.addRow(coloumnData);
            }
                    
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }
    
    
